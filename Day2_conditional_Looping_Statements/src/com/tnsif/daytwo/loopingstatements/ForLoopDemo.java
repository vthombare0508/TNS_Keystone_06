package com.tnsif.daytwo.loopingstatements;

public class ForLoopDemo {

	public static void main(String[] args) {

		for(int i=1; i<=10; i++)
		{
			System.out.println("values of i : "+i);
		}
		
		System.out.println("---------------------");
		for(int j=10; j>=1; j--)
		{
			System.out.println("Value of J : "+ j);
		}
	}

}
